Tower.prototype.run = function () {
    for (var i = 0; i < bullets.length; i++) {
        var b = bullets[i];
        if (dist(b.x, b.y, this.x, this.y) <= 30 && b.al && !b.dead) {
            this.health -= b.damage;
            b.dead = true;
        }
    }

    imageMode(CENTER);
    var shooting = false;
    if (this.fireType !== "missile") {
        for (var i = 0; i < enemies.length; i++) {
            var e = enemies[i];
            var cond = dist(e.x, e.y, this.x, this.y) < this.range + e.rad && !e.dead;
            if (cond) {
                //this.r += (atan2(e.y - this.y, e.x - this.x) - this.r) / 10;

                if (this.targets === 1) {
                    this.r = atan2(mouseY - this.y, mouseX - this.x);
                } else {
                    this.r = atan2(e.y - this.y, e.x - this.x);
                }
                shooting = true;
            }
        }
    } else {
        shooting = true;
    }

    if (this.selected) {
        fill(0, 0, 0, 125);
        ellipse(this.x, this.y, 100, 100);
    }
    push();
    translate(this.x, this.y);
    if (dist(mouseX, mouseY, this.x, this.y) < 20 && !this.selected) {
        fill(255, 255, 255, 50);
        strokeWeight(1);
        stroke(200);
        ellipse(0, 0, this.range * 2, this.range * 2);
        _txt(this.name, 1, 21, 0, 10, true);
        _txt(this.name, -1, 19, 0, 10, true);
        _txt(this.name, 1, 19, 0, 10, true);
        _txt(this.name, -1, 21, 0, 10, true);
        _txt(this.name, 0, 20, 2, 10, true);
        csr = sprites.cursorp;
    }
    this.ar += (this.r - this.ar) / 5;
    image(sprites[this.base], 0, 0);
    rotate(this.ar);
    if (frameCount % this.rate === 0 && shooting) {
        translate(-cos(this.r) * 3, -sin(this.r) * 3);
    }
    image(sprites[this.t], 8, 0);
    pop();
    if (this.shielded) {
        if (this.maxHealth !== 500) {
            this.health = 500;
            this.maxHealth = 500;
        }
        if (this.health > 350) {
            image(sprites.shield1, this.x, this.y);
        } else if (this.health > 250 && this.health <= 350) {
            image(sprites.shield2, this.x, this.y);
        } else if (this.health <= 250 && this.health > 100) {
            image(sprites.shield3, this.x, this.y);
        }
    }

    if (this.health < this.maxHealth) {
        fill(255, 0, 0);
        rect(this.x - 15, this.y - 15 - 5, 15 * 2, 2);
        fill(0, 255, 0);
        rect(
            this.x - 15,
            this.y - 15 - 5,
            (this.health / this.maxHealth) * (15 * 2),
            2
        );
    }

    if (this.selected) {
        fill(255, 255, 255, 25);
        stroke(255);
        ellipse(this.x, this.y, 20, 20);
        image(sprites.upg, this.x, this.y - 30 + sin(frameCount) * 2.5, 20, 20);
        image(
            sprites.sll,
            this.x - 20,
            this.y - 20 + cos(frameCount) * 2.5,
            20,
            20
        );
        image(
            sprites.trg,
            this.x + 20,
            this.y - 20 - cos(frameCount) * 2.5,
            20,
            20
        );
        if (
            dist(mouseX, mouseY, this.x - 20, this.y - 20 + cos(frameCount) * 2.5) <
            10
        ) {
            _txt(
                "+$" + Math.round(this.cost * 0.75),
                this.x,
                this.y + 20,
                2,
                10,
                true
            );
            csr = sprites.cursorp;
            if (clicked) {
                money += Math.round(this.cost * 0.75);
                this.dead = true;
                this.selected = false;
            }
        }
        if (
            dist(mouseX, mouseY, this.x, this.y - 30 + sin(frameCount) * 2.5) < 10
        ) {
            csr = sprites.cursorp;
            if (money >= this.nextCost && this.next != "max") {
                if (clicked) {
                    money -= this.nextCost;
                    this.t = this.next;
                    updateTower(this);
                    this.selected = false;
                }
                _txt("-$" + this.nextCost, this.x, this.y + 20, 2, 10, true);
            } else if (this.next == "max") {
                csr = sprites.cursorn;
                _txt("Maxed", this.x, this.y + 20, 3, 10, true);
            } else {
                csr = sprites.cursorn;
                _txt("-$" + this.nextCost, this.x, this.y + 20, 3, 10, true);
            }
        }
        if (
            dist(mouseX, mouseY, this.x + 20, this.y - 20 - cos(frameCount) * 2.5) <
            10
        ) {
            if (!this.shielded) {
                if (money >= 100) {
                    csr = sprites.cursorp;
                    if (clicked) {
                        this.shielded = true;
                        money -= 100;
                    }
                    _txt("-$100", this.x, this.y + 20, 2, 10, true);
                } else {
                    csr = sprites.cursorn;
                    _txt("-$100", this.x, this.y + 20, 3, 10, true);
                }
            } else {
                csr = sprites.cursorn;
                _txt("Maxed", this.x, this.y + 20, 3, 10, true);
            }
        }
    }

    if (dist(mouseX, mouseY, this.x, this.y) < 20 && clicked) {
        for (var i = 0; i < towers.length; i++) {
            if (towers[i].selected) {
                towers[i].selected = false;
            }
        }
        this.selected = true;
    }
    if (shooting) {
        if (frameCount % this.rate == 0) {
            if (this.fireType == "single") {
                bullets.push(
                    new Bullet(
                        this.bulletType,
                        this.x + cos(this.r) * 10,
                        this.y + sin(this.r) * 10,
                        {
                            damage: this.damage,
                            dot: this.dot,
                            slowdown: this.slowdown,
                            pierce: this.pierce,
                            r: this.ar,
                            speed: this.speed,
                        }
                    )
                );
            }
            if (this.fireType == "double") {
                bullets.push(
                    new Bullet(
                        this.bulletType,
                        -5 + this.x + cos(this.r) * 10,
                        this.y + sin(this.r) * 10,
                        {
                            damage: this.damage,
                            dot: this.dot,
                            slowdown: this.slowdown,
                            pierce: this.pierce,
                            r: this.ar,
                            speed: this.speed,
                        }
                    )
                );
                bullets.push(
                    new Bullet(
                        this.bulletType,
                        5 + this.x + cos(this.r) * 10,
                        this.y + sin(this.r) * 10,
                        {
                            damage: this.damage,
                            dot: this.dot,
                            slowdown: this.slowdown,
                            pierce: this.pierce,
                            r: this.ar,
                            speed: this.speed,
                        }
                    )
                );
            }
            if (this.fireType == "missile") {
                for (var i = 0; i < this.range; i++) {
                    bullets.push(new Bullet("b20", Math.random() * 1200, -100, {
                        damage: this.damage,
                        r: 0,
                        dot: 0,
                        slowdown: 0,
                        pierce: 0,
                        speed: 0
                    }));
                }
            }
        }
    }

    if (this.health <= 0) {
        this.dead = true;
    }
};
