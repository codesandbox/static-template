function handelclose() {
  var x = document.getElementById("slidbar");
  if (x.classList[x.classList.length - 1] === "show") {
    x.classList.remove("show");
    x.classList.add("hide");
  }
}
function myFunction() {
  var x = document.getElementById("slidbar");
  if (x.classList[x.classList.length - 1] === "show") {
    x.classList.remove("show");
    x.classList.add("hide");
  } else {
    x.classList.remove("hide");
    x.classList.add("show");
  }
}
